"""## 6. Backend Prediction Function

This is what your backend calls. Takes `station_id`, `month`, `year` and returns predicted precipitation in inches.

"""

def predict_precipitation(station_id: str, month: int, year: int, bundle: dict) -> float:
    """
    Predict accumulated precipitation for a given station, month, and year.

    Inputs (from frontend via backend):
        station_id: 3-letter CDEC code (e.g. "SHA")
        month: 1-12
        year: e.g. 2027

    Returns:
        Predicted precipitation in inches
    """
    sm_stats = bundle["station_month_stats"]
    s_stats = bundle["station_stats"]
    trends = bundle["trend_df"]
    s_map = bundle["station_map"]
    base_yr = bundle["base_year"]

    sm = sm_stats[(sm_stats["STATION_ID"] == station_id) & (sm_stats["month"] == month)]
    ss = s_stats[s_stats["STATION_ID"] == station_id]
    tr = trends[trends["STATION_ID"] == station_id]

    hist_mean = sm["hist_mean"].values[0]
    hist_median = sm["hist_median"].values[0]
    hist_std = sm["hist_std"].values[0]
    hist_min = sm["hist_min"].values[0]
    hist_max = sm["hist_max"].values[0]
    station_mean = ss["station_mean"].values[0]
    station_max = ss["station_max"].values[0]
    yearly_trend = tr["yearly_trend"].values[0]

    year_offset = year - base_yr
    month_sin = np.sin(2 * np.pi * month / 12)
    month_cos = np.cos(2 * np.pi * month / 12)
    trend_projection = hist_mean + (year_offset * yearly_trend / 12)
    hist_cv = hist_std / (hist_mean + 0.01)

    features = pd.DataFrame([{
        "station_encoded": s_map[station_id],
        "month": month,
        "year_offset": year_offset,
        "month_sin": month_sin,
        "month_cos": month_cos,
        "hist_mean": hist_mean,
        "hist_median": hist_median,
        "hist_std": hist_std,
        "hist_min": hist_min,
        "hist_max": hist_max,
        "station_mean": station_mean,
        "station_max": station_max,
        "yearly_trend": yearly_trend,
        "trend_projection": trend_projection,
        "hist_cv": hist_cv,
    }])

    prediction = bundle["model"].predict(features)[0]
    return max(0.0, round(prediction, 2))


