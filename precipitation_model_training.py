df = pd.read_csv(CSV_PATH, encoding="utf-8")
df.columns = df.columns.str.strip()

# Clean VALUE column
df["VALUE"] = df["VALUE"].astype(str).str.replace(",", "", regex=False).str.strip()
df["VALUE"] = pd.to_numeric(df["VALUE"], errors="coerce")

# Parse datetime
df["DATE TIME"] = pd.to_datetime(df["DATE TIME"], errors="coerce")

# Drop missing
df = df.dropna(subset=["VALUE", "DATE TIME"])

# Extract month and year
df["month"] = df["DATE TIME"].dt.month
df["year"] = df["DATE TIME"].dt.year

print(f"Total rows: {len(df)}")
print(f"Stations: {sorted(df['STATION_ID'].unique())}")
print(f"Date range: {df['DATE TIME'].min().date()} to {df['DATE TIME'].max().date()}")
