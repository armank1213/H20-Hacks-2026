"""## 3. Feature Engineering

Since the model only receives **station + month + year** from the frontend, features are built from historical statistical profiles and trend analysis. No lag features (we won't have "last month's value" for future predictions).

"""

# Historical stats per station+month
station_month_stats = df.groupby(["STATION_ID", "month"])["VALUE"].agg(
    hist_mean="mean",
    hist_median="median",
    hist_std="std",
    hist_min="min",
    hist_max="max",
).reset_index()

# Per-station overall stats
station_stats = df.groupby("STATION_ID")["VALUE"].agg(
    station_mean="mean",
    station_max="max",
).reset_index()

# Yearly trend per station (linear slope of annual total precipitation)
yearly_totals = df.groupby(["STATION_ID", "year"])["VALUE"].sum().reset_index()
yearly_totals.columns = ["STATION_ID", "year", "yearly_total"]

station_trends = {}
for station in df["STATION_ID"].unique():
    s = yearly_totals[yearly_totals["STATION_ID"] == station]
    if len(s) > 2:
        coeffs = np.polyfit(s["year"], s["yearly_total"], 1)
        station_trends[station] = coeffs[0]
    else:
        station_trends[station] = 0.0

trend_df = pd.DataFrame(list(station_trends.items()), columns=["STATION_ID", "yearly_trend"])

print("Yearly precipitation trends (inches/year):")
for _, row in trend_df.iterrows():
    direction = "drying" if row["yearly_trend"] < 0 else "wetting"
    print(f"  {row['STATION_ID']}: {row['yearly_trend']:>+.3f} in/year ({direction})")

# Merge all features into main dataframe
df = df.merge(station_month_stats, on=["STATION_ID", "month"], how="left")
df = df.merge(station_stats, on=["STATION_ID"], how="left")
df = df.merge(trend_df, on=["STATION_ID"], how="left")

# Encode station as integer
station_map = {s: i for i, s in enumerate(sorted(df["STATION_ID"].unique()))}
df["station_encoded"] = df["STATION_ID"].map(station_map)

# Year offset
base_year = df["year"].min()
df["year_offset"] = df["year"] - base_year

# Cyclical month encoding
df["month_sin"] = np.sin(2 * np.pi * df["month"] / 12)
df["month_cos"] = np.cos(2 * np.pi * df["month"] / 12)

# Trend projection for this station+month
df["trend_projection"] = df["hist_mean"] + (df["year_offset"] * df["yearly_trend"] / 12)

# Coefficient of variation (how variable is precip at this station+month)
df["hist_cv"] = df["hist_std"] / (df["hist_mean"] + 0.01)  # avoid div by zero

# Feature columns
feature_cols = [
    "station_encoded",
    "month",
    "year_offset",
    "month_sin",
    "month_cos",
    "hist_mean",
    "hist_median",
    "hist_std",
    "hist_min",
    "hist_max",
    "station_mean",
    "station_max",
    "yearly_trend",
    "trend_projection",
    "hist_cv",
]

X = df[feature_cols]
y = df["VALUE"]

print(f"Feature matrix shape: {X.shape}")
print(f"Target stats: mean={y.mean():.2f}, std={y.std():.2f}, min={y.min():.2f}, max={y.max():.2f}")

"""## 4. Model Training & Evaluation

**Strategy:** Hold out the last 2 years (2024–2025) for testing. Train on 2000–2023. This simulates how the model would perform predicting future values it hasn't seen.

"""

